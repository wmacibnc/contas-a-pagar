
-- CRIAÇÃO DE TABELAS
CREATE TABLE CONTA (
ID INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
DESCRICAO VARCHAR(50),
VALOR DOUBLE,
PARCELAS INT,
ATIVO TINYINT(1),
REG_DATE DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP );

CREATE TABLE PAGAMENTO (
ID INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
ID_CONTA INT(6),
MES INT,
ANO INT,
REG_DATE DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP );

